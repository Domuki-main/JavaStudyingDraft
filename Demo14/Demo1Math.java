package Java20200401.Demo14;

public class Demo1Math {

    //1. abs(double num) absolute number
    //2. ceil(double num) get int from up  5.1 -> 6
    //3. floor(double num) get int from down  5.8 -> 5
    //4. round(double num) 4 down 5

    //5. Math.PI

    public static void main(String[] args) {
        System.out.println(Math.abs(-12));
        System.out.println(Math.ceil(1.2));
        System.out.println(Math.floor(1.2));
        System.out.println(Math.round(23.455));
    }

}
