package Java20200401.Demo9;

public class StudentInDemo9 {

    private String name;
    private int age;

    public StudentInDemo9() {
    }

    public StudentInDemo9(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
